da = input('Digite algo:')
print('O tipo primitivo deste valor é:', type(da))
print('Só tem espaço?', da.isspace())
print('É um número?', da.isnumeric())
print('É alfabético?', da.isalpha())
print('É alfanúmerico?', da.isnumeric())
print('Está em maiúscula?', da.isupper())
print('Está em minúscula?', da.islower())
print('Esta Capitalizada?', da.istitle())


